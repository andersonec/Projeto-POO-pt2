    private void confirmarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarItemActionPerformed
        // TODO add your handling code here:
        quantidadeProduto = quantidadeProdutos.getValue().toString();
        int quantidade = Integer.parseInt(quantidadeProduto);
        if(produtoCompra.equals("Camisa")){
            precoProduto += camiseta*quantidade;
            total += camiseta*quantidade;
        }
        else if(produtoCompra.equals("Calça")){
                precoProduto += calca*quantidade;
                total += calca*quantidade;
        }
        else if(produtoCompra.equals("Sapato")){
                precoProduto += sapato*quantidade;
                total += sapato*quantidade;
        }
        else{
            JOptionPane.showMessageDialog(null, "Produto enviado para o carrinho!");
        }
        totalCarrinhoP = formatar.format(total);
        quantidadeCarrinho.append(quantidadeProduto + "\n");
        produtoCarrinho.append(produtoCompra + "\n");
        precoCarrinho.append(precoProduto + "\n");
        totalCarrinho.setText(totalCarrinhoP);
        salvarCompra += "\n\tQUANTIDADE: " + quantidadeProduto + 
                        "\n\tPRODUTO: " + produtoCompra +
                        "\n\tTOTAL PRODUTO: " + precoProduto;
        produtoVenda.setText(null);
        categoriaProduto.setText(null);
        quantidadeProdutos.setValue(1);
        precoProduto = "";
    }//GEN-LAST:event_confirmarItemActionPerformed

