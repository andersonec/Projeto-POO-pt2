    private void confirmarVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarVendaActionPerformed
        // TODO add your handling code here:
        if(quantidadeCarrinho == null){
            JOptionPane.showMessageDialog(null, "Quantidade nula, recomece a venda!");
        }
        else if(produtoCarrinho == null){
            JOptionPane.showMessageDialog(null, "Sem produtos no Carrinho, recomece a venda!");
        }
        else if(precoCarrinho == null){
            JOptionPane.showMessageDialog(null, "Não existe preço no Carrinho, recomece a venda!");
        }
        else{
            ManipularArquivo escreveCompra = new ManipularArquivo();
            try{
                salvarCompra += "\n\n\tTOTAL COMPRA: " + totalCarrinhoP + ";\n";
                escreveCompra.escreverArquivo(vendasConcluidas, salvarCompra);
                JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
                dispose();
            }catch(IOException except){
                except.printStackTrace();
            }
        }
    }//GEN-LAST:event_confirmarVendaActionPerformed

