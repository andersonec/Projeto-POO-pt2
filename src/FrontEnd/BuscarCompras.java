    private void buscarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarCActionPerformed
        // TODO add your handling code here:
        int posicao = 0;
        try {
            FileReader listarFile = new FileReader(clientesCad);
            BufferedReader listarBuff = new BufferedReader(listarFile);
            FileReader listarCompra = new FileReader(comprasCliente);
            BufferedReader listarCompraB = new BufferedReader(listarFile);
            imprimirNome.mapearArquivo(clientesCad);
            imprimirCpf.mapearArquivo(clientesCad);
            for (int i = 0; i < clientesCad.length(); i++) {
                String linhaL = listarBuff.readLine();
                if(codigoB.equals(linhaL)){
                    posicao = i + 1;
                    imprimirNome.setClienteEncontrado(posicao);
                    imprimirCpf.setClienteEncontrado(posicao + 1);
                    clienteBusca = codigoB + "\n" + imprimirNome.getClienteEncontrado() +
                    "\n" + imprimirCpf.getClienteEncontrado();
                    infoCliente.append(clienteBusca);
                    JOptionPane.showMessageDialog(null, "Cliente encontrado!");
                }
            }
            listarBuff.close();
            listarFile.close();
        } catch (IOException except) {
            except.printStackTrace();
        }
    }//GEN-LAST:event_buscarCActionPerformed

    private void okBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBuscaActionPerformed
        // TODO add your handling code here:
        try {
            FileReader listarFile = new FileReader(comprasCliente);
            BufferedReader listarBuff = new BufferedReader(listarFile);
            imprimirNome.mapearArquivo(comprasCliente);
            imprimirCpf.mapearArquivo(comprasCliente);
            for (int i = 0; i < comprasCliente.length(); i++) {
                String linhaL = listarBuff.readLine();
                if(("CF3A" + codigoB + "NF4B").equals(linhaL)){
                    int posicao = i + 1;
                    imprimirNome.setClienteEncontrado(posicao);
                    imprimirCpf.setClienteEncontrado(posicao + 1);
                    clienteBusca = ("NUMERO DA NOTA FISCAL: CF3A" + codigoB + "NF4B") + "\n" + imprimirNome.getClienteEncontrado() +
                    "\n" + imprimirCpf.getClienteEncontrado();
                    comprasRealizadas.append(clienteBusca);
                    JOptionPane.showMessageDialog(null, "Cliente encontrado!");
                }
            }
            listarBuff.close();
            listarFile.close();
        } catch (IOException except) {
            except.printStackTrace();
        }
    }//GEN-LAST:event_okBuscaActionPerformed

